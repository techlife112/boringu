      <div class="section-title max-title mb-5" id="Oxygen">
        <h3>Oxygen on Earth?</h3>
      </div>
      <p class="textpara">
        Our forests, rivers, oceans and soils provide us with the food we eat, the air we breathe, the water we irrigate our crops with. We also rely on them for numerous other goods and services we depend on for our health, happiness and prosperity.
      </p>
      <p class="textpara">
        Oxygen in atmosphere is only 21%. And that is produced the most from oceans and forests. At least half of Earth's oxygen comes from the ocean.
      </p>
      <p class="textpara">
        The surface layer of the ocean is teeming with photosynthetic plankton. Though they're invisible to the naked eye, they produce more oxygen than the largest redwoods. Calculating the exact percentage of oxygen produced in the ocean is difficult because the amounts are constantly changing. Scientists estimate that 50-80% of the oxygen production on Earth comes from the ocean.
      </p>
      <p class="textpara">
        Forest produces 20% of oxygen in atmosphere. While it is virtually impossible to know how many trees are in the world, satellite imaging has helped procure a rough estimate. A study in the journal of 'Nature' reported close to 3.04 trillion trees on earth. And though this might seem like a lot- it's not! 3.04 trillion trees make for almost 422 trees per person.
      </p>

